package cubeConundrum;

public class Show {
    private int redCount;
    private int greenCount;
    private int blueCount;

    public Show(int r, int g, int b) {
        redCount = r;
        greenCount = g;
        blueCount = b;
    }

    public int getRedCount() {
        return redCount;
    }

    public int getGreenCount() {
        return greenCount;
    }

    public int getBlueCount() {
        return blueCount;
    }

}
